#include<iostream>
using namespace std;
int main(){
    int ror=15 | 30; /* result or bitwise operator*/
    int rand=15 & 30; /* result and bitwise operator*/
    int rnot=~15; /*result not bitwise operator*/
    int rxor=15^30; /*result xor bitwise operator*/
    int rls=15<<2; /*result of left shift opeartor*/
    int rrs=15>>2; /* result of right shift bitwise operator*/
    cout<<ror<<endl;
    cout<<rand<<endl;
    cout<<rnot<<endl;
    cout<<rxor<<endl;
    cout<<rls<<endl;
    cout<<rrs<<endl;



}